def c_to_f(celsius):
    fahrenheit = (celsius * 9/5) + 32
    return fahrenheit

def f_to_c(fahrenheit):   
    celsius = (fahrenheit - 32) * 5/9
    return celsius

def km_to_miles(kilometers):
    miles = kilometers * 0.621371
    return miles

def miles_to_km(miles):
    kilometers = miles / 0.621371
    return kilometers